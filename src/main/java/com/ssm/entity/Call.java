package com.ssm.entity;

public class Call {

	int id;
	int callerId;
	int calleeId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCallerId() {
		return callerId;
	}

	public void setCallerId(int callerId) {
		this.callerId = callerId;
	}

	public int getCalleeId() {
		return calleeId;
	}

	public void setCalleeId(int caleeId) {
		this.calleeId = caleeId;
	}
}
